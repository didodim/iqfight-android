package com.empters.iqfight.utils;

import android.widget.ImageView;
import android.widget.TextView;

public class AnswerView {
	private ImageView answerView;
	private TextView answerText;

	public ImageView getAnswerView() {
		return answerView;
	}
	public void setAnswerView(ImageView answerView) {
		this.answerView = answerView;
	}
	public TextView getAnswerText() {
		return answerText;
	}
	public void setAnswerText(TextView answerText) {
		this.answerText = answerText;
	}

}
